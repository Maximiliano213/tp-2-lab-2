def area_circulo(radio):
    """Calcula el área de un círculo dado su radio."""
    return math.pi * radio * radio

def ex_c6_area_circulo():
    print("\n--- Ejercicio C6: Área de un círculo ---")
    try:
        r = float(input("Ingresá el radio (número): "))
    except ValueError:
        print("Entrada inválida.")
        return
    if r < 0:
        print("El radio no puede ser negativo.")
        return
    a = area_circulo(r)
    print(f"Área del círculo de radio {r} = {a:.4f}")

def suma_lista(numeros):
    """Devuelve la suma de los elementos de la lista 'numeros'."""
    return sum(numeros)

def ex_c7_suma_lista():
    print("\n--- Ejercicio C7: Sumar lista de números ---")
    entrada = input("Ingresá números separados por espacios (ej: 1 2 3.5): ")
    if not entrada.strip():
        print("No ingresaste números.")
        return
    try:
        lista = [float(x) for x in entrada.split()]
    except ValueError:
        print("Entrada inválida. Asegurate de ingresar solo números separados por espacios.")
        return
    total = suma_lista(lista)
    print(f"Lista: {lista}")
    print(f"Suma: {total}")

