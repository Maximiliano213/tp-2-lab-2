def ex_a3_tipado():
    """Ej 3: Mostrar el tipo de cada variable usando type()"""
    print("\n--- Ejercicio A3: Mostrar tipos con type() ---")
    var_str = "texto"
    var_int = 5
    var_float = 3.14
    var_bool = False
    var_none = None

    variables = [("var_str", var_str), ("var_int", var_int), ("var_float", var_float), ("var_bool", var_bool), ("var_none", var_none)]
